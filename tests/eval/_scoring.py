"""Shared scoring helpers for Engine-output eval fixtures.

Two public entry points:

- :func:`score_brief` — grade a ``BriefResult`` + case against the play-brief
  rubric (schema, length, no verbatim book prose, chain references present,
  valid citation tokens).
- :func:`score_halftime` — grade a halftime response (bullets[]) against the
  halftime rubric (exactly 3 bullets, bullet-length band, no verbatim book
  prose, at least one must-contain token).

Both return a ``Signals`` dict (signal name → bool + rationale). Callers
decide whether to fail the test on any False signal or track subscores over
time.

The forbidden-phrase list lives at ``eval/forbidden_phrases.json`` and is
generated by ``eval/build_forbidden_phrases.py`` from the compiled insight
sidecars. A match against any phrase on any user-facing surface violates
the "books never surface" rule (CLAUDE.md, 2026-04-24).
"""

from __future__ import annotations

import json
import re
from pathlib import Path
from typing import Any, TypedDict

# ---------------------------------------------------------------------------

_BACKEND = Path(__file__).resolve().parent.parent.parent
_FORBIDDEN_PATH = _BACKEND / "eval" / "forbidden_phrases.json"

# Max Sn index in the source registry (S1..S16 per memory wiki-ingest-operations).
_MAX_SOURCE_ID = 16

_CITATION_RE = re.compile(r"\[S(\d+)(?:,\s*pp?\.\s*[\d\-–]+)?\]")  # noqa: RUF001 (EN DASH intentional — matches typographic page ranges)


class Signal(TypedDict):
    pass_: bool
    detail: str


Signals = dict[str, Signal]


def _signal(passed: bool, detail: str = "") -> Signal:
    return {"pass_": passed, "detail": detail}


def load_forbidden_phrases() -> list[str]:
    """Load the forbidden-phrase blocklist. Fails loudly if missing — caller
    should regenerate via ``python eval/build_forbidden_phrases.py``."""
    if not _FORBIDDEN_PATH.exists():
        raise FileNotFoundError(
            f"{_FORBIDDEN_PATH} missing — run `uv run python eval/build_forbidden_phrases.py`"
        )
    return json.loads(_FORBIDDEN_PATH.read_text(encoding="utf-8"))


def _check_forbidden(text: str, phrases: list[str]) -> Signal:
    lower = text.lower()
    for p in phrases:
        if p.lower() in lower:
            return _signal(False, f"contains forbidden phrase: {p[:60]!r}")
    return _signal(True, "no verbatim book prose detected")


def _normalize_for_match(slug: str) -> list[str]:
    """Return surface forms to check for in output text for a slug.

    ``hip_flexor_complex`` → ``["hip_flexor_complex", "hip flexor complex"]``.
    ``drill-360-jump``     → ``["drill-360-jump", "drill 360 jump", "360 jump"]``.
    """
    forms = {slug}
    if "_" in slug:
        forms.add(slug.replace("_", " "))
    if "-" in slug:
        forms.add(slug.replace("-", " "))
        # Drop the "drill-" / "exercise-" prefix so "drill-ankling" matches
        # "ankling" or "barrier-jump-cut-sprint" in a sentence that might
        # not restate the prefix — add both the dashed and spaced forms.
        parts = slug.split("-", 1)
        if len(parts) == 2 and parts[0] in {"drill", "exercise", "concept"}:
            stripped = parts[1]
            forms.add(stripped)
            forms.add(stripped.replace("-", " "))
    # Basketball-voice synonyms for anatomy slugs. Form-coach briefs
    # translate clinical anatomy ("ankle_complex") into how a coach speaks
    # ("ankles", "feet"). The eval rubric must accept those translations or
    # it would force the prompt to regress to clinical jargon — see
    # feedback-coach-voice-not-clinical.md.
    forms.update(_ANATOMY_BASKETBALL_FORMS.get(slug, ()))
    return [f for f in forms if f]


_ANATOMY_BASKETBALL_FORMS: dict[str, tuple[str, ...]] = {
    "ankle_complex": ("ankle", "ankles", "feet"),
    "elbow_complex": ("elbow", "elbows", "shooting elbow"),
    "shoulder_girdle": ("shoulder", "shoulders", "shoulder line"),
    "wrist_complex": ("wrist", "wrists", "shooting hand"),
    "hip_flexor_complex": ("hip", "hips", "hip flexor"),
    "hip_complex": ("hip", "hips"),
    "core_outer": ("core", "midsection", "trunk"),
    "core": ("core", "midsection"),
    "glute_max": ("glutes", "hip", "hips", "butt"),
    "knee": ("knee", "knees"),
}


def _any_of_slugs(text: str, slugs: list[str]) -> tuple[bool, str]:
    lower = text.lower()
    for slug in slugs:
        for form in _normalize_for_match(slug):
            if form in lower:
                return True, f"matched {slug!r} via form {form!r}"
    return False, f"none of {slugs!r} surfaced"


def _citations_valid(text: str, known_citations: list[str]) -> Signal:
    """Every [Sn] / [Sn, pp.X] in ``text`` must resolve to S1..S_MAX. Any
    output citation should also appear in the bundle's known citation set
    (prevents the composer from fabricating a citation the bundle doesn't
    back)."""
    known_lower = {c.lower() for c in known_citations}
    for match in _CITATION_RE.finditer(text):
        idx = int(match.group(1))
        if not 1 <= idx <= _MAX_SOURCE_ID:
            return _signal(False, f"citation {match.group(0)!r} out of S1..S{_MAX_SOURCE_ID}")
        if known_lower and match.group(0).lower() not in known_lower:
            return _signal(
                False, f"citation {match.group(0)!r} not in bundle citations"
            )
    return _signal(True, "all inline citations valid")


# --- play-brief rubric ------------------------------------------------------


def score_brief(
    *,
    brief_text: str,
    source_citations: list[str],
    case: dict[str, Any],
    forbidden_phrases: list[str],
    bundle_anatomy: list[str] | None = None,
    bundle_drills: list[str] | None = None,
) -> Signals:
    """Grade a brief against the play-brief rubric.

    ``bundle_anatomy`` / ``bundle_drills`` are the runtime chain for the
    play — passing them lets ``anatomy_ref`` / ``drill_ref`` pass if *any*
    chain slug surfaces. The JSONL's ``must_reference_any_of.*`` list is
    treated as an authorial hint that is unioned with the bundle, so a
    case is stable against graph additions.
    """
    exp = case.get("expectations", {})
    ref = exp.get("must_reference_any_of", {})
    out: Signals = {}
    text = brief_text or ""

    out["non_empty"] = _signal(bool(text.strip()), f"len={len(text)}")

    min_len = int(exp.get("min_length", 0))
    max_len = int(exp.get("max_length", 10_000))
    length_ok = min_len <= len(text) <= max_len
    out["length_ok"] = _signal(length_ok, f"len={len(text)} in [{min_len},{max_len}]")

    out["no_forbidden"] = _check_forbidden(text, forbidden_phrases)

    anatomy_candidates = list(
        dict.fromkeys((ref.get("anatomy") or []) + (bundle_anatomy or []))
    )
    if anatomy_candidates:
        passed, detail = _any_of_slugs(text, anatomy_candidates)
        out["anatomy_ref"] = _signal(passed, detail)
    else:
        out["anatomy_ref"] = _signal(True, "no anatomy candidates to match")

    drill_candidates = list(
        dict.fromkeys((ref.get("drills") or []) + (bundle_drills or []))
    )
    if drill_candidates:
        passed, detail = _any_of_slugs(text, drill_candidates)
        out["drill_ref"] = _signal(passed, detail)
    else:
        out["drill_ref"] = _signal(True, "no drill candidates to match")

    out["valid_citations"] = _citations_valid(text, source_citations)

    must_contain = exp.get("must_contain_any") or []
    if must_contain:
        lower = text.lower()
        hit = next((tok for tok in must_contain if tok.lower() in lower), None)
        out["must_contain_any"] = _signal(
            bool(hit), f"matched {hit!r}" if hit else f"none of {must_contain!r} present"
        )

    return out


# --- halftime rubric --------------------------------------------------------


def score_halftime(
    *,
    bullets: list[str],
    case: dict[str, Any],
    forbidden_phrases: list[str],
) -> Signals:
    exp = case.get("expectations", {})
    out: Signals = {}

    expected_count = int(exp.get("bullet_count", 3))
    out["bullet_count"] = _signal(
        len(bullets) == expected_count,
        f"got {len(bullets)} bullets, expected {expected_count}",
    )

    min_b = int(exp.get("min_bullet_len", 1))
    max_b = int(exp.get("max_bullet_len", 1000))
    offenders = [
        (i, len(b)) for i, b in enumerate(bullets) if not (min_b <= len(b) <= max_b)
    ]
    out["length_ok"] = _signal(
        not offenders,
        f"all bullets in [{min_b},{max_b}]" if not offenders else f"offenders={offenders}",
    )

    joined = "\n".join(bullets)
    out["no_forbidden"] = _check_forbidden(joined, forbidden_phrases)

    must_contain = exp.get("must_contain_any") or []
    if must_contain:
        joined_lower = joined.lower()
        hit = next((tok for tok in must_contain if tok.lower() in joined_lower), None)
        out["must_contain_any"] = _signal(
            bool(hit),
            f"matched {hit!r}" if hit else f"none of {must_contain!r} across bullets",
        )

    return out


def failed_signals(signals: Signals) -> list[str]:
    """Return signal names that failed, sorted, for error-message use."""
    return sorted(name for name, s in signals.items() if not s["pass_"])


# --- practice-generator rubric ----------------------------------------------


def score_practice_brief(
    *,
    plan: list[dict[str, Any]],
    source_citations: list[str],
    case: dict[str, Any],
    forbidden_phrases: list[str],
    bundle_anatomy: list[str] | None = None,
    bundle_drills: list[str] | None = None,
) -> Signals:
    """Grade a practice plan against the v0 rubric.

    `plan` is a list of `{drill_slug, duration_minutes, reasoning, cross_refs}`
    dicts (the JSON-shape of PracticeBlockOut). `case` carries:

    - ``min_blocks`` / ``max_blocks`` — accepted block-count band
    - ``duration_tolerance_pct`` — sum of block durations must be within
      this percent of the requested ``duration_minutes``
    - ``reasoning_min_length`` / ``reasoning_max_length`` — concatenated
      reasoning across all blocks
    - ``must_reference_any_of.anatomy`` — at least one anatomy slug appears
      in concatenated reasoning
    - ``must_contain_any`` — at least one keyword appears (case-insensitive)
    """
    exp = case.get("expectations", {})
    ref = exp.get("must_reference_any_of", {})
    out: Signals = {}

    out["non_empty"] = _signal(len(plan) > 0, f"{len(plan)} blocks")

    min_blocks = int(exp.get("min_blocks", 4))
    max_blocks = int(exp.get("max_blocks", 7))
    block_count_ok = min_blocks <= len(plan) <= max_blocks
    out["block_count_ok"] = _signal(
        block_count_ok, f"{len(plan)} blocks in [{min_blocks},{max_blocks}]"
    )

    requested = int(case.get("duration_minutes", 60))
    summed = sum(int(b.get("duration_minutes") or 0) for b in plan)
    tol_pct = float(exp.get("duration_tolerance_pct", 15))
    lo = requested * (1 - tol_pct / 100)
    hi = requested * (1 + tol_pct / 100)
    out["duration_balanced"] = _signal(
        lo <= summed <= hi,
        f"sum={summed}min, requested={requested}min, tol=±{tol_pct}%",
    )

    concat = " ".join(b.get("reasoning") or "" for b in plan)
    min_len = int(exp.get("reasoning_min_length", 200))
    max_len = int(exp.get("reasoning_max_length", 3000))
    out["length_ok"] = _signal(
        min_len <= len(concat) <= max_len,
        f"concat={len(concat)}chars in [{min_len},{max_len}]",
    )

    out["no_forbidden"] = _check_forbidden(concat, forbidden_phrases)

    anatomy_candidates = list(
        dict.fromkeys((ref.get("anatomy") or []) + (bundle_anatomy or []))
    )
    if anatomy_candidates:
        passed, detail = _any_of_slugs(concat, anatomy_candidates)
        out["anatomy_ref"] = _signal(passed, detail)
    else:
        out["anatomy_ref"] = _signal(True, "no anatomy candidates to match")

    # Every block names a drill via drill_slug — we just verify each is in the
    # bundle if a bundle is supplied (i.e. the engine didn't invent a drill).
    if bundle_drills:
        bundle_set = set(bundle_drills)
        unknown = [
            b.get("drill_slug")
            for b in plan
            if b.get("drill_slug") not in bundle_set
        ]
        out["drill_ref"] = _signal(
            not unknown,
            "all drills in bundle"
            if not unknown
            else f"unknown drills: {unknown}",
        )
    else:
        out["drill_ref"] = _signal(True, "no drill bundle to validate against")

    out["valid_citations"] = _citations_valid(concat, source_citations)

    must_contain = exp.get("must_contain_any") or []
    if must_contain:
        lower = concat.lower()
        hit = next((tok for tok in must_contain if tok.lower() in lower), None)
        out["must_contain_any"] = _signal(
            bool(hit),
            f"matched {hit!r}" if hit else f"none of {must_contain!r} present",
        )

    return out
